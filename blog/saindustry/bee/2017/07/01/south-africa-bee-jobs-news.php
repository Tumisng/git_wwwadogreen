<h2 id="the-average-salary-in-south-africa-vs-the-world">The average salary in South Africa vs the world</h2>

<h4 id="the-organisation-for-economic-co-operation-and-development-oecd-has-updated-its-data-on-the-average-wage-paid-to-workers-across-the-36-countries-it-covers">The Organisation for Economic Co-operation and Development (OECD) has updated its data on the average wage paid to workers across the 36 countries it covers.</h4>

<p>The latest jobs data by StatsSA showed a quarter -on-quarter increase of 0.8% in average monthly earnings paid to employees in the formal non-agricultural sector to R18,687 in February 2017.</p>

<p>BankservAfrica’s Disposable Salary Index (BDSI) showed a more realistic ‘take home’ picture averaging at R13,790 in May 2017, slightly lower than April’s R13,914.</p>

<p>The OECD data uses the total wages paid out to employees, hence the Stats SA data makes for the best comparison. It should also be noted that the OECD is also adjusted for purchasing power parity.</p>

<p>Jobs data by StatsSA for 2016 showed that average monthly earnings paid to employees in the formal non-agricultural sector was at R17,517 per month – translating to R210,200 annually.</p>

<p>South Africa’s average salary per year translates to $15,861, but the country has a local purchasing power almost twice the value of the US$ – a fair value exchange of R5.81 to the dollar, according to the IMF (2016).</p>

<p>Compared to the most recently available OECD data, South Africa falls somewhere in the middle of the country’s covered, with the dollar equivalent (US$36,180, adjusted for purchasing power parity) placing around the same levels as Italy and Spain.</p>

