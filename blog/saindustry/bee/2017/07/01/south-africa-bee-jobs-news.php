                <h2 id="more-people-on-grants-than-with-jobs-five-years-later">More people on grants than with jobs, five years later</h2>

<h4 id="johannesburg---there-were-more-than-17-million-south-africans-dependent-on-social-grants-on-a-monthly-basis-compared-to-about-155-million-south-africans-with-jobs-last-year-the-sa-institute-of-race-relations-said-on-wednesday">Johannesburg - There were more than 17 million South Africans dependent on social grants on a monthly basis compared to about 15.5 million South Africans with jobs last year, the SA Institute of Race Relations said on Wednesday.</h4>

<p>In a finding released in the institute’s latest South Africa Survey, it said this did not bode well for the economy because government would find it harder to expand the rollout of grants or to increase their value.</p>

<p>“The numbers are a recipe for social and political chaos,” the institute’s analyst Gerbrandt van Heerden said.</p>

<p>“As the economy stagnates, and tax revenue slows, demand for more grants will increase. The government will then have to cut other areas of expenditure in order to meet popular demands for more and higher grants. We predict that this will lead to much higher levels of violent protest action.”</p>

