<p>Two recent but disparate events bring Black Economic Empowerment (BEE) into sharp focus.</p>

<p>The first is the new Mining Charter, the second, the predicament of the Democratic Alliance (DA) in light of its leadership crisis.</p>

<p>Sara Gon is a Policy Fellow at the IRR, a think tank that promotes economic and political liberty
There are multitudinous criticisms of BEE: it benefits only a small elite, it has progressed from voluntary compliance to obligatory quotas, its processes are complex, and it has failed to take account of the impact of economic and financial realities on its implementation.</p>

<p>For the first time, the mining industry is staring down a politically compromised and dishonest minister in Mosebenzi Zwane.</p>

<p>“This charter’s not going to see the light of day anytime soon,” says mining law expert Peter Leon. “We’re looking at years of protracted litigation.” (‘Zwane’s controversial mining charter won’t see light of day’ – analyst, Paul Burkhardt and Kevin Crowley, BizNews, June 19, 2017).</p>

<p>In the same article, Ben Davis of Librium Capital Limited says: “The value destruction is hard to quantify and the uncertainty will persist. What is certain is that South Africa continues to be a terrible destination for mining investment and assets in South Africa will continue to trade at a discount”.</p>

<p>In a letter to Business Day on June 20, 2017, John Louw asks incredulously how a charter can force mining companies to pay dividends over a 10-year period to facilitate the repayment of finance extended to empowerment “partners”, with a penalty if the dividends fall short of extinguishing such debt at the end of the period? This imposition on a listed company is contrary to the most elementary principles of capital management. Dividends should only be declared and paid by the directors of a company after due consideration of numerous factors, not least of which are ongoing liquidity and solvency.</p>

<p>Carol Paton points out (By Playing two sides, Malusi Gigaba takes a gamble on the economy’s strength, Business Day, June 20, 2017) that it was no coincidence that at his press conference on 15 June, 2017, Gigaba promised imminent policy certainty through inter alia the Mining Charter.</p>

