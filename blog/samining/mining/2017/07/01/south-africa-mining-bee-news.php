<h2 id="minister-defends-new-mining-charter">MINISTER DEFENDS NEW MINING CHARTER</h2>

<h4 id="johannesburg--mineral-resources-minister-mosebenzi-zwane-has-said-that-the-controversial-2017-mining-charter-gazetted-last-week-was-meant-to-be-a-catalyst-that-provides-practical-expression-to-the-countrys-goal-of-a-more-inclusive-economy">Johannesburg – Mineral Resources Minister Mosebenzi Zwane has said that the controversial 2017 Mining Charter gazetted last week was meant to be a catalyst that provides practical expression to the country’s goal of a more inclusive economy.</h4>

<p>“We encourage the young people who are the future of this country to embrace the Mining Charter by exploiting the opportunities to be unleashed by this instrument of change,” Zwane said.</p>

<p>“We will be embarking on provincial roadshows in the next two weeks to raise awareness and to take the Charter to the people.”</p>

<p>Zwane said this when he was tabling the R1.779 billion Budget Vote of the Mineral Resources Department in the National Council of Provinces on Wednesday.</p>

<p>The reviewed Mining Charter has caused a lot of uncertainty for stakeholders and the markets by setting new black ownership targets for the industry.</p>

<p>The Chamber of Mines vehemently rejected it, saying that the department had not held meaningful consultations before the introduction of some of the items, and thus it would approach the courts to interdict its implementation.</p>

<p>The targets include new mining rights, holders having 30 percent black ownership to be shared among employees, communities and black entrepreneurs. Mining rights holders who have complied with the previous target of 26 percent have to “top up” to 30 percent within 12 months.</p>

<p>Those applying for prospecting rights would be required to have a “minimum of 50 percent plus one black person shareholding”. These shareholders must have voting rights.</p>

<p>The National Union of Mineworkers (NUM), on the other hand, welcomed the reviewed Mining Charter, saying that it appreciated the increase from the initial 26 percent to the 30 percent minimum BEE shareholding in the industry.</p>

<p>Zwane said the majority of the people of South Africa who make up 90 percent of the population remained excluded from the economy. He said the economy remained lopsided, unequal and non-inclusive because of the legislative framework.</p>

<p>Zwane said this was a huge detriment to South Africa’s socio-economic growth efforts, adding that the need for radical economic</p>

<p>transformation was more imperative than ever before because it sought to redress the institutionalised monopoly of the economy.</p>

<p>“Economic reforms are needed to ensure broader and inclusive participation to enable the attainment of a far more inclusive and competitive economy,” Zwane said.
