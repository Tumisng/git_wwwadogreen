es through his mind. – Colin Collard</h4>

<p>“The mining industry has been the bedrock of the South African economy for over 100 years. The sector has made many positive contributions to the development of our economy, and some of the infrastructure and industries thriving today are as a direct resulting of mining.</p>

<p>“We cannot deny, however, that there have been some undesirable effects, brought on by decades of colonialism and apartheid.</p>

<p>“Unemployment, inequality and poverty have been growing over the years, creating an untenable situation.</p>

<p>“Of course, people like to blame the ANC for the widespread unemployment. They say we lack ideas, are anti-business, don’t know what we are doing, etc. But we’ve been in power for, what is it, just under a quarter of a century—that isn’t long, is it?</p>

<p>“What is comforting is that today a poor rural shack dweller visiting the city can see many blacks who are rich, indeed some who are very rich. So, even though it is taking a little bit longer than we wish, we are getting there, aren’t we? Anyway, we’d like to spread this type of wealth around a little more. And that’s the purpose of this Mining Charter.</p>

<p>“The birth of South Africa’s democracy in 1994 brought about political freedom for all. Vast economic imbalances remain a challenge, particularly when the majority of South Africans have yet to see meaningful economic participation or ownership of these minerals.</p>

<p>“Naturally, I am not discounting the 40% of earnings the mining industry contributes to government in tax, a rate which many say is among the highest in the world. And I am not denigrating the mining industry’s contributions by way of licenses or its royalty payments to communities. Or that mining employs half a million people who in turn pay taxes to the government. I am also not unmindful of the dividends the mining companies pay to the state’s Industrial Development Corporation and to the government employees’ pension fund. Or even the R200 billion that has already been transferred to black individuals or organisations in empowerment deals. We, as government, see none of these contributions as a problem.</p>

