<h2 id="deterring-investment-101-zwane-wants-to-up-bee-mine-rule-despite-warnings">Deterring Investment 101: Zwane wants to up BEE mine rule despite warnings</h2>

<h4 id="in-what-could-be-the-final-kicks-of-a-dying-horse-the-zuptoid-faction-in-the-anc-continues-to-push-through-poorly-cobbled-together-regulations-as-part-of-their-radical-economic-transformation-rhetoric-bloomberg-is-reporting-that-mines-minister-mosebenzi-zwane-of-the-famed-2016-bank-probe-saga-and-a-close-gupta-ally-wants-to-up-black-ownership-of-mining-assets-from-26-percent-to-30-percent-this-is-despite-criticism-from-the-industry-and-even-some-parts-of-his-party-if-the-anc-boots-out-zuma-at-its-nec-meeting-this-coming-weekend-one-hopes-that-it-will-be-the-end-of-days-for-zwane-transformation-must-happen--but-it-must-be-realistic-and-shouldnt-harm-what-is-still-one-of-south-africas-most-key-sectors--gareth-van-zyl">In what could be the final kicks of a dying horse, the Zuptoid faction in the ANC continues to push through poorly cobbled together regulations as part of their ‘radical economic transformation‘ rhetoric. Bloomberg is reporting that Mines Minister Mosebenzi Zwane (of the famed 2016 bank probe saga and a close Gupta ally) wants to up black ownership of mining assets from 26 percent to 30 percent. This is despite criticism from the industry and even some parts of his party. If the ANC boots out Zuma at its NEC meeting this coming weekend, one hopes that it will be the end of days for Zwane. Transformation must happen – but it must be realistic and shouldn’t harm what is still one of South Africa’s most key sectors. – Gareth van Zyl</h4>

<p>(Bloomberg) – South African Mines Minister Mosebenzi Zwane has proposed raising the mandatory black ownership of mining assets to 30 percent from 26 percent, drawing opposition from some ruling-party officials who fear it will deter investment, two people familiar with the situation said.</p>

<p>The proposal is part of a long-delayed draft mining charter outlined by Zwane, an ally of President Jacob Zuma, to the African National Congress’ economic policy committee on May 13.</p>

<p>Senior party policy officials warned of the potential negative consequences of his plans, said the people, who asked not to be identified because Zwane hasn’t formally proposed the changes for public comment before they become binding.</p>

<p>Zuma’s cabinet on Wednesday approved the draft mining charter, which will be released for public comment once it has been gazetted. ANC spokesman Zizi Kodwa didn’t answer calls seeking comment. It’s unclear whether the cabinet demanded changes.</p>

<p>South Africa’s Chamber of Mines said this week that the government needs to finalise its mining regulations if falling investment in the industry is to be reversed. Zuma, who’s due to step down as leader of the ANC in December and as the nation’s president in 2019, has called for “radical economic transformation” to more fairly distribute the benefits of South Africa’s economy among the black majority.</p>

