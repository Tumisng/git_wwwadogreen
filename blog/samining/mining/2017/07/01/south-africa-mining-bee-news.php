                <h2 id="zwane-goes-rogue-as-he-seeks-to-bulldoze-through-new-bee-mining-regulations">Zwane goes rogue as he seeks to bulldoze through new BEE mining regulations</h2>

<h4 id="bloomberg--south-africas-chamber-of-mines-said-it-will-refuse-to-attend-a-last-minute-meeting-with-the-government-on-thursday-as-the-mines-minister-prepares-to-release-revised-regulations-for-the-industry">(Bloomberg) — South Africa’s Chamber of Mines said it will refuse to attend a last-minute meeting with the government on Thursday as the mines minister prepares to release revised regulations for the industry.</h4>

<p>A process to review and amend South Africa’s mining charter “has been concluded” and Mineral Resources Minister Mosebenzi Zwane will hold a press briefing at 10:00 am in Pretoria, the capital, on Thursday, the Department of Mineral Resources said in an emailed statement on Wednesday.</p>

<p>The Chamber of Mines, which represents the nation’s biggest operators including Anglo American Plc and Sibanye Gold Ltd., received an invitation on Wednesday afternoon for a meeting with the ministry that is scheduled for an hour before the start of the press briefing, the industry lobby said in an emailed statement.</p>

<p>The chamber “will not be co-opted into participating in an attempt by the department to provide any support into what we believe has been a flawed process,” it said in the statement. The ministry has “not seen fit” to engage properly with stakeholders about the content of the charter, the chamber said.</p>

