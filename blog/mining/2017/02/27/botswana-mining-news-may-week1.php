<h2 id="controversial-ckgr-mine-based-ghaghoo-suspends-operations">Controversial CKGR mine-based Ghaghoo suspends operations</h2>

<h5 id="a-relatively-new-diamond-mine-located-in-the-heart-of-central-kalahari-game-reserve-ckgr-ghaghoo-mine-which-is-a-subsidiary-of-gem-diamonds-botswana-has-suspended-operations-and-preparing-to-undergo-retrenchment-exercise">A relatively new diamond-mine, located in the heart of Central Kalahari Game Reserve (CKGR), Ghaghoo mine which is a subsidiary of Gem Diamonds Botswana, has suspended operations and preparing to undergo retrenchment exercise.</h5>

<p>Managing Director of Gem Diamonds Botswana, Haile Mphusu, confirmed to this publication that retrenchment will ensue as a result of halting of production at the mine. “We are starting process of putting the mine on care and maintenance,” Mphusu pointed out while adding that in a lay man’s view, “it means that production has stopped.” “The only work that will take place is the one aimed at preserving the assets. By all intends and purposes production has ceased,” the MD stressed.</p>

<p>The reasons for closing the mine he said is that the price of the type of diamonds that is mined at Ghaghoo has weakened and “we are continuing to lose money.” He pointed out that most employees will be cut but of course not all will lose jobs, others will remain to keep the asset in state so that when prices of diamonds recovers, mining can start.</p>

<p>“We have around 183 employees in Botswana who work for Gem Diamonds Botswana that owns Ghaghoo mine. We also have around 115 that we outsourced to other companies. In the process of care and maintenance, only around 30 to 40 employees will remain to take care of the assets.  Therefore around 250 will lose jobs,” he highlighted to this publication.</p>

<p>According to Mphusu, shareholders cannot continue to subsidies the mine. Gem Diamonds, the leading producer of high-value diamonds, owns both the Letšeng mine in Lesotho, and the Ghaghoo mine in Botswana. Its client base comprises of prominent diamantaires and manufacturers from the world’s major diamond centers which are New York, Belgium, Israel, India, China, South Africa, the United Kingdom, the Middle East and Switzerland.</p>

