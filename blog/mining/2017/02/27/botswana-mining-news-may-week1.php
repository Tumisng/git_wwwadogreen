<h2 id="botswana-gets-offer-for-struggling-bcl-mines">Botswana gets offer for struggling BCL mines</h2>

<h5 id="feb-7-botswanas-high-court-agreed-on-tuesday-to-delay-the-provisional-liquidation-of-state-owned-bcl-mine-ltd-after-lawyers-representing-the-liquidator-kpmg-said-they-had-received-an-offer-to-buy-its-mothballed-mines-which-produce-copper-and-nickel">Feb 7 Botswana’s high court agreed on Tuesday to delay the provisional liquidation of state-owned BCL Mine Ltd after lawyers representing the liquidator KPMG said they had received an offer to buy its mothballed mines, which produce copper and nickel.</h5>

<p>While the lawyers declined to name the company making the offer, a source close to the process told Reuters a company from the United Arab Emirates had put forward an offer for the three companies under the BCL group.</p>

<p>“The minister is currently in the UAE negotiating for the sale of the BCL group,” said the source, who declined to be named as the matter was confidential.</p>

<p>The Minister of Minerals, Energy and Green Technology Sadique Kebonang posted on his Facebook page on Monday a picture of himself captioned: “In the UAE trying to save BCL.”</p>

<p>In a briefing in January, Nigel Dixon-Warren of KPMG said he would recommend to the courts that BCL be placed under final liquidation as its three subsidiaries were insolvent and the government had no money to finance operations.</p>

<p>Following the placement of BCL group under provisional liquidation in October 2016, Russia’s Norilsk took legal action against the mining group to recover $271.3 million it says it is owed for the sale of a 50 percent stake in the Nkomati JV in South Africa.</p>

<p>Apart from the Norilsk claim, BCL owes creditors including suppliers and banks around $85.41 million. ($1 = 10.5374 pulas) (Reporting by Johannesburg Newsroom, editing by David Evans)</p>

