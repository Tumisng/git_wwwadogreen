<h2 id="shumba-signs-option-for-75-stake-in-botswana-coal-mine">Shumba signs option for 75% stake in Botswana coal mine</h2>

<h5 id="shumba-energy-has-signed-an-option-to-acquire-75-of-the-morupule-south-project-in-botswana-from-hodges-resources">Shumba Energy has signed an option to acquire 75% of the Morupule South project in Botswana from Hodges Resources.</h5>

<p>The Morupule South Coal project is located within the central eastern district of Botswana, directly to the south of the Morupule Colliery. The project has a resource of 2,45bn tons of Joint Ore Reserves Committee (JORC) compliant resources of which 380m tons are in the measured and indicated categories.</p>

<p>The Morupule main seam represents 83% of the resources, and 1,2bn tons are amenable to open cut mining with initial waste to coal strip ratios of under 2 to 1.</p>

<p>Study results confirm robust economics across all Morupule South development scenarios. Wood Mackenzie benchmarked Morupule South’s mining costs and coal quality against other southern African domestic mines and the project is expected to be one of the lowest cost in the region.</p>

<p>Morupule South’s mining costs and coal quality were benchmarked against other south African domestic mines. The energy adjusted benchmarking graph supports the viability of the project’s development.</p>

<p>Shumba Energy’s managing director, Mashale Phumaphi, comments, “The Morupule South acquisition elevates Shumba Energy to be able to take advantage of the domestic and regional industrial market.</p>

<p>“The project is located five kilometres from an operating rail siding and can be brought into production within a very short timeframe. These factors set the Morupule South project ahead of other coal development projects in Botswana.”</p>

