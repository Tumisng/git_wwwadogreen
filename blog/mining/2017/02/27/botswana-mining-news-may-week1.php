                <h2 id="botswana-adopts-canadas-towards-sustainable-mining-initiative">Botswana adopts Canada’s Towards Sustainable Mining initiative</h2>

<h4 id="gaborone-botswana-feb-14-2017-cnw---the-botswana-chamber-of-mines-bcm-today-announced-that-it-will-adopt-the-towards-sustainable-mining-tsm-initiative-a-corporate-social-responsibility-program-developed-by-the-mining-association-of-canada-mac-to-improve-environmental-and-social-practices-in-the-mining-industry">GABORONE, Botswana, Feb. 14, 2017 /CNW/ - The Botswana Chamber of Mines (BCM) today announced that it will adopt the Towards Sustainable Mining® (TSM) initiative, a corporate social responsibility program developed by the Mining Association of Canada (MAC) to improve environmental and social practices in the mining industry.</h4>

<p>MAC and its members launched TSM in 2004. Implementation of the program is mandatory for all MAC members’ Canadian operations, but many voluntarily apply it to their international sites. MAC freely shares TSM with other countries seeking tools to improve the environmental and social performance of their mining industries, including engagement with civil society and enhanced transparency and accountability.</p>

<p>TSM requires mining companies to annually assess their facilities’ performance across six important areas, including tailings management, community outreach, safety and health, biodiversity conservation, crisis management, and energy use and greenhouse gas emissions management. The results are freely available to the public and are externally-verified every three years to ensure what has been reported is accurate. While BCM will tailor its performance areas so that they reflect the unique aspects of its domestic mining sector, they will be at a similar level to those of Canada’s.</p>

<p>To ensure TSM reflects the expectations of civil society and industry stakeholders, it was designed and continues to be shaped by an independent, multi-interest advisory panel. As part of its implementation, BCM will implement a similar advisory body to provide this valuable oversight function.</p>

