<h2 id="pangolin-diamonds-to-drill-kimberlite-targets-at-motloutse-diamond-project-botswana">Pangolin Diamonds to Drill Kimberlite Targets at Motloutse Diamond Project, Botswana</h2>

<p>TORONTO, ONTARIO–(Marketwired - May 31, 2016) - Pangolin Diamonds Corp. (TSX VENTURE:PAN) (the “Company” or “Pangolin”) is pleased to provide an update for the Company’s wholly-owned Motloutse Diamond Project (“Motloutse”), located 150 km east of the Orapa Diamond Mine in Botswana. Several aeromagnetic anomalies have been selected for follow-up with soil sampling, ground magnetic and gravity surveys to identify kimberlite drill targets. This program is nearing completion.</p>

<p>The AGA-01 kimberlite target was confirmed on the ground to be associated with a gravity anomaly which coincides with positive kimberlitic indicator minerals. Sub-angular kimberlite indicator garnets with sculptured surfaces, denoting proximity to source, were optically identified from five soil samples collected directly over the gravity anomaly associated with the AGA-01 aeromagnetic anomaly. Both the gravity and soil sampling surveys at AGA-01 are being expanded to cover a larger area.</p>

<p>Additional kimberlite targets are being further developed using detailed gravity surveys expected to be completed by mid-June. Once the gravity results have been interpreted, the drill program will commence and include the additional targets identified. It is anticipated that the percussion drill program will be completed before the end of July 2016.</p>

<p>The Motloutse Diamond Project covers the area where the first diamonds in Botswana were recovered in 1959. It also includes the location where De Beers discovered its first diamonds in Botswana in 1962. The kimberlite source(s) of these diamonds have never been located.</p>

<p>The indicators were recovered at AGA-01 from unscreened 100-litre samples collected on a 50 m x 50 m square grid within a 10 square metre area of a GPS-controlled sample site. The material was dry screened in the field to recover the +0.425-2.0 millimetre size fraction, then transported to Francistown, Botswana and processed through Pangolin’s 1-tph DMS plant. The concentrates were subsequently delivered to an independent mineral specialist in Gaborone, Botswana who examined the concentrate and recovered the indicators.</p>

