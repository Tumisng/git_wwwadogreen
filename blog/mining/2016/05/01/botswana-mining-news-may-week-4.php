<p>Four BCL miners perished Sunday evening as preliminary information suggests that a rope snapped while a cage was transporting workers at end of shift. The weekend incident has shattered the fatality-prone BCL mine safety priorities which have been questionable as for the past two years (2014/2015) the mine recorded the highest number of fatalities in the sector, with 11 deaths.</p>

