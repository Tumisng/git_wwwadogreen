<h2 id="botswana-water-infrastructure-needs-rehabilitation">Botswana: Water Infrastructure Needs Rehabilitation</h2>

<h5 id="gaborone--the-alarming-water-crises-in-some-areas-is-said-to-be-exacerbated-by-high-water-leakage-and-inadequate-infrastructure-resulting-in-the-water-utilities-corporation-wuc-shutting-down-the-inflow">Gaborone — The alarming water crises in some areas is said to be exacerbated by high water leakage and inadequate infrastructure resulting in the Water Utilities Corporation (WUC) shutting down the inflow.</h5>

<p>This was said by the permanent secretary in the Ministry of Minerals, Energy and Water Resources Mr Kgomotso Abi before the Public Accounts Committee (PAC) adding that the situation called for rehabilitation of the infrastructure to avoid the leakages.</p>

<p>He however said rehabilitation had already commenced in Mahalapye and WUC was looking to rehabilitate the borehole in Maun to help the water situation in Maun.</p>

<p>Mr Abi denied allegations that the water crisis was caused by low capacities following the transfer of services from Water Affairs noting that WUC had adequate staff capacity because they retained people from water affairs and council.</p>

<p>Responding to questions from MP for Nata Gweta Mr Polson Majaga who asked for an update with regard to underground water drilling, the he stated that work was undergoing as they have teams doing site seeing for underground water drilling to augment the water crises.</p>

<p>Answering a question from MP for Francistown West Mr Ignatius Moswaane who wanted to know how true were the reports that there are people who had dug private dams along the rivers that supply Gaborone dam Mr Abi admitted this was true however said these were small dams that had a capacity less that 400 million cubic litres all combined.</p>

