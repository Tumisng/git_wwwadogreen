<h2 id="two-titans-drag-down-the-bse">Two titans drag down the BSE</h2>

<h4 id="sechaba-brewery-holdings-limited-stock-price-has-plunged-the-most-since-the-beginning-of-this-year-the-fall-in-sechaba-share-price-is-an-occurrence-repeated-across-the-wholesaling-and-retail-sector-in-the-botswana-stock-exchange-marked-by-falling-share-prices-amid-tough-operating-conditions">Sechaba Brewery Holdings limited stock price has plunged the most since the beginning of this year. The fall in Sechaba share price, is an occurrence repeated across the wholesaling and retail sector in the Botswana Stock Exchange, marked by falling share prices amid tough operating conditions.</h4>

<p>The wholesaling and retail sector has dragged the BSE’s domestic company index (DCI) down by 3.61% since the beginning of the year. The sector’s drag spills over from last year when the DCI declined by 11.3% after the wholesaling and retail sector lost 7.1% to become the worst performing sector in the local stock market. This was a reversal of fortunes from 2015 when the DCI finished the year 11.6% up with the wholesale and retail sector being the second best performing sector at 4.2%.</p>

<p>The performance of the sector this year reflects a broader trend that started earlier last year when African states battled with the fall in commodity prices due to waning demand across major markets. Furthermore wholesaling and retail stocks were under pressure from the El Nino phenomenon that affected many farmers, particularly in Southern Africa. By nature of their business, wholesaling and retail companies’ revenue is directly affected by the spending power of consumers and any negative shocks to the economy that affects government and consumer spending power rattles investors.</p>

<p>Sechaba’s shock share price drop was rapid but not totally unexpected. The group has so far lost 22% of its share value amid a challenging environment that investors have no appetite for. The woes of Sechaba dates back to the introduction of the alcohol levy in 2008. The levy has had an impact on the volumes shipped thus reducing the bottom line margins. However, the brewery giant was able to remain resistant, delivering profits and investors cheering them on the stock market.</p>

<p>A spanner was once more thrown in the works when the government introduced the traditional beer regulation that affected some of the group’s operations. The combination of the levies and regulations are now weighing heavily on Sechaba’s operations. To compound the matter further, investors are weary now considering the future of the group under new owners: in 2015, Anheuser-Busch InBev finally offered SABMiller PLC £68 billion in a  takeover bid, creating a brewing giant making about a third of the world’s beer.</p>

