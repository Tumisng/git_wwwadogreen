<h4 id="the-edcon-group-last-week-began-to-run-a-series-of-workshops-aimed-at-capacitating-the-local-textile-industry-to-improve-the-quality-of-their-products-while-also-looking-to-conform-to-the-specific-demands-of-the-retailer-businessweeks-pauline-dikuelo-attended-the-workshop-and-discovered-that-after-years-of-limiting-themselves-to-the-local-market-an-opportunity-that-needs-to-be-grabbed-with-both-hands-has-risen-for-local-textile-players-to-break-the-export-boundary-and-supply-the-retailing-group-in-their-outlets-spread-across-seven-countries-such-as-south-africa-zimbabwe-namibia-zambia-lesotho-and-mozambique">The Edcon Group last week began to run a series of workshops aimed at capacitating the local textile industry to improve the quality of their products while also looking to conform to the specific demands of the retailer. BusinessWeek’s PAULINE DIKUELO attended the workshop and discovered that after years of limiting themselves to the local market, an opportunity that needs to be grabbed with both hands, has risen for local textile players to break the export boundary and supply the retailing group in their outlets spread across seven countries such as South Africa, Zimbabwe, Namibia, Zambia, Lesotho and Mozambique</h4>

<p>The workshop followed the last November’s Ministry of Investment, Trade and Industry engagement with them and other retailers to start giving preference to local suppliers.</p>

<p>This was a condition set by government as a way of ‘arm twisting’ foreign retailers  operating in reserved businesses to empower Batswana. The Ministry says it have reached a mutual satisfactory agreement with the South African retailer consenting to capacitate local suppliers as well as giving a part of their in-store floor space to local suppliers in the next two years.</p>

<p>As part of the agreement, Edcon will avail 10% of its floor space to local suppliers in the two years leading to December 2018. The exemption would allow them to trade in the clothing retail space reserved for citizens, with Edcon agreeing to facilitate and run supplier development workshops in Botswana on agreed dates.</p>

