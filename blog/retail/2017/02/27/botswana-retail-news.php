<h2 id="believe-it-or-not-serobe-in-a-can">Believe it or not, serobe in a can</h2>

<h4 id="at-one-point-marcia-thabo-sentos-dream-was-to-become-an-interior-designer-the-profession-she-studied-for-at-curtin-university-in-australia-in-2004">At one point, Marcia Thabo Sento’s dream was to become an interior designer, the profession she studied for at Curtin University in Australia, in 2004.</h4>

<p>Having completed her studies, she planned to venture into her chosen career, but fate would intervene.</p>

<p>Sento married her long-time sweetheart Webster Mothusi Sento. The couple’s marriage vows resulted in a change of dreams, from interior design, to venturing into a unique business enterprise. That new dream has resulted in the birth of a unique Botswana product - tripe  (serobe) in a can.</p>

<p>In recent months, the canned serobe has garnered increasing attention from the public, with many enquiring where it can be purchased. The canned tripe, available in plain and chilly 410 grammes, is proving a surprise hit, especially as more Batswana move towards the quick and easy convenience of microwave meals.</p>

<p>Recently, the Tsabong-raised Marcia told the story of how her career path turned from an interest in interior design, to helping her husband set up a packaging and catering enterprise that is taking the retail market by storm.</p>

<p>Sento quit everything and threw her focus into her husband’s idea of packaging serobe.</p>

<p>“I love my husband so much that I sacrificed everything to work with him on the product. It was not easy to start the business as we encountered a lot of exhausting challenges on the road to where we are now. We continue to strive of course, but that’s everyday business,” she said.</p>

