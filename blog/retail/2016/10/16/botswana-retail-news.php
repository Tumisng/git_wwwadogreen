<h2 id="parly-has-surrendered-to-khama--manual-workers">Parly has surrendered to Khama – Manual Workers</h2>

<h4 id="manual-workers-union-has-accused-parliament-of-abdicating-its-function-to-the-president-allowing-him-to-do-as-he-pleases-in-the-appointment-of-the-judges-of-the-court-of-appeal-coa">Manual Workers union has accused Parliament of abdicating its function to the President, allowing him to do as he pleases in the appointment of the judges of the Court of Appeal (CoA).</h4>

<p>The union, under the banner of the National Amalgamated Local Central Government and Parastatal Workers Union (NALCGPWU), said according to the Constitution the number of justices of appeal was prescribed by Parliament instead of the President.</p>

<p>The union, in its legal challenge to Khama over his appointment of the seven justices of the appeal, argued at the Lobatse High Court this week that Parliament instead surrendered its responsibilities to the President.</p>

<p>The veteran union led by Johnson Motshwarakgole is also challenging the constitutional validity of Section 4 of the CoA Act in so far as it delegates, to the President, Parliament’s constitutional powers to determine the number of justices. They are seeking a declaratory order to that effect.</p>

<p>Alec Freund (SC) submitted that in respect of Section 99 (2) (b) of the Constitution, the provision requires that the number if any of the justices of appeal in addition to others must be prescribed by Parliament.</p>

<p>“Despite the fact that Section 99 (2) (b) requires Parliament to prescribe the number, if any, Parliament has given away its powers to the president.</p>

<p>It has left the number to be determined by discretionary decisions,” he said.</p>

<p>He explained that moreover the power conferred on Parliament by the latter section was a legislative power, which was required to be exercised by enacting an Act in the manner prescribed by Section 88 of the Constitution.</p>

