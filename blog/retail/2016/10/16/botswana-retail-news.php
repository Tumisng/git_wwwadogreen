<h2 id="botswanas-mobile-internet-use-surges">Botswana’s mobile internet use surges</h2>

<h4 id="mobile-internet-usage-in-the-country-is-becoming-more-pervasive-as-most-people-are-getting-their-hands-on-more-mobile-devices">Mobile internet usage in the country is becoming more pervasive as most people are getting their hands on more mobile devices.</h4>

<p>This is according to Statistics Botswana (SB), which recently released a report that reflects on the country’s household access and individual use of Information and Communications Technology (ICT) in 2014.</p>

<p>The data-collecting agency says 40.6% of households had access to internet in the year under review, adding that the majority of households with access to internet used mobile internet.  This constitutes 94.3% of all households with access to internet.</p>

<p>“In 2014, 85.3% of the population 10 years and over in Botswana used a mobile cellular telephone. Of these individuals, 44.2% were males while 55.8% were females,” reads the report.</p>

<p>SB further states that the most common device used to access the internet was a mobile cellular telephone, noting that most internet users, 78.4%, used it to participate in social networks.</p>

<p>It says those who used the internet to read or download online newspapers, magazines or electronic books amounted to 56.6% of internet users.</p>

<p>Internet banking and getting information from government departments’ websites were performed by 8.6% and 21.1% respectively of internet users.</p>

<p>“The majority of mobile cellular telephone users spend most of their time chatting, sending and receiving short messages as well as playing music and videos,” the report states.</p>

<p>According to SB, Gaborone had the largest proportion of mobile cellular telephone users in 2014, with 17.5% of the total, while Kweneng East and Ngwaketse districts followed with 14.6% and 6.3% respectively.</p>

