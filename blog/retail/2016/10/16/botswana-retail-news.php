<h2 id="brexit-might-trigger-epa-revision">Brexit might trigger EPA revision</h2>

<h4 id="the-recent-vote-for-the-united-kingdom-uk-to-leave-the-european-union-eu-might-lead-to-the-renegotiation-of-the-recently-signed-economic-partnership-agreement-epa-economic-experts-believe-that-by-breaking-away-from-the-eu-the-uk-would-cease-to-be-party-to-any-of-the-eus-trade-agreements-including-the-epa">The recent vote for the United Kingdom (UK) to leave the European Union (EU) might lead to the renegotiation of the recently signed Economic Partnership Agreement (EPA). Economic experts believe that by breaking away from the EU, the UK would cease to be party to any of the EU’s trade agreements including the EPA.</h4>

<p>The EU and Botswana are important trading partners. Available data from Statistics Botswana shows that in December 2010, the country’s total exports were valued at P1.8 billion and almost half of them, P910 million, went to the EU.</p>

<p>The UK has been the largest import partner of Botswana, importing goods and services valued at more than P820 million. For December 2010, almost 45% of Botswana’s exports went to the UK.</p>

<p>In the latest economic review by Econsult Botswana, Keith Jefferis asserted that for Botswana, Brexit adds to uncertainty, given that the country’s beef is exported to the UK under the recently signed EPA trade agreement with the EU.</p>

<p>“This uncertainty will remain for quite some time, until the form of post-Brexit trade and economic relations between the UK, the EU and the rest of the world become much clearer,” he said.</p>

<p>He stated that the UK’s exit from the EU shows that people are prepared to support something that is not in their economic self-interest, if other factors are perceived to be more important.</p>

<p>“Certainly, it has had a destabilising effect on the global economy, not to mention the UK economy. For instance, the proposed mega-merger between the brewers ABInbev and SABMiller may well fall apart, because it is no longer as attractive given that it is priced in devalued post-Brexit sterling,” Jefferis said.</p>

<p>He further noted that global growth forecasts have been revised downwards slightly, post-Brexit, adding that nobody knows how these will play out.</p>

<p>Meanwhile, the Botswana Meat Commission, the sole exporter of the local beef to the EU, is on record saying it does not anticipate any immediate impact on its exports to both the UK and EU.</p>

