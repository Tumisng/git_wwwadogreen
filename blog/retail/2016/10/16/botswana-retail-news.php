                <h2 id="bakgatla-leaders-petition-govt-over-sa-retailers-ban">Bakgatla leaders petition govt over SA retailers ban</h2>

<h4 id="the-bakgatla-leaders-senior-chiefs-representative-kgosi-segale-linchwe-mochudi-west-mp-gilbert-mangole-and-kgatleng-district-council-are-in-a-fighting-mood">The Bakgatla leaders, Senior Chief’s representative, Kgosi Segale Linchwe, Mochudi West MP Gilbert Mangole and Kgatleng District Council are in a fighting mood.</h4>

<p>They are up in arms against the government following the Minister of Investment, Trade and Industry’s refusal to grant the South African retailers licences to operate in the Kgatleng P100million Pilane mall.</p>

<p>After a compromise was reached in the initial dispute, in which Prime Time Properties Ltd had written to request the ministry to exempt non-citizen retailers reserved business activities and allow them to expand their operations in the same line of trade, a new dispute has erupted in which the government now wants the SA retailers to buy local products.</p>

<p>The defiant retailers amongst them Jet, Mr Price, Clicks, KFC and others are still struggling on how to accede to this ultimatum with some arguing that they are already buying some of the local products such as towels.</p>

<p>The mall currently has 28 tenants, amongst them; First National Bank (FNB), Choppies Supermarket, Cashbuild, Puma petrol station, Barclays Bank, Knock-Out Cash ‘n Carry, OK Furniture and cell phone service providers.</p>

<p>Bakgatla, it is understood, are disappointed with the mall, which they long anticipated as an alternative for Gaborone. Conveniently built in the heart of Kgatleng District, the mall is expected to serve villages like Bokaa, Morwa, Pilane, Mochudi, Rasesa, Mmathubudukwane, Sikwane and Malolwane among others.</p>

