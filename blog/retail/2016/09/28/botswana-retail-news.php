                <h2 id="choppies-pays-hefty-price-for-expansion">Choppies pays hefty price for expansion</h2>

<p>CHOPPIES Enterprises — the leading supermarket grocery retailer in Botswana — continued with its expansion strategy into the rest of Africa in the year to end-June, but the moves into new territories came with a more than 50% cost to the company’s earnings.</p>

<p>Choppies reported headline earnings per share of 7.25 thebe (about 9.48c) in the period under review, down from 16.92 thebe a year earlier. Turnover, however, increased 24% to 7.4-billion pula, while gross profit rose 12% to 1.44-billion pula.</p>

<p>CEO Ramachandran Ottapathu said Choppies’s full-year performance had been affected by pre-operative expenses in Zambia and Kenya. He said the company had also incurred losses in Zimbabwe due to depressed economic conditions, which resulted in a shortage of cash in circulation. The knock-on effect had been a decline in purchases per transaction.</p>

<p>Choppies, not unlike Shoprite — which is the biggest retailer in Africa — has ambitious growth targets for the continent. The retailer lists its focus as East Africa, with emphasis on Tanzania and Kenya. Shoprite has yet to successfully branch out in East Africa. According to PwC, Kenya and Tanzania are among 10 countries with high potential for retail growth in sub-Saharan Africa.</p>

