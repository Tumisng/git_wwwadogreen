<h2 id="botswana-concerns-over-on-and-off-net-call-costs">Botswana: Concerns Over On and Off-Net Call Costs</h2>

<h4 id="on-the-backdrop-of-an-outcry-for-reduction-of-internet-data-prices-in-the-neighbouring-south-africa-an-expert-engaged-by-the-botswana-communications-regulatory-authority-bocra-has-revealed-that-there-is-no-evidence-of-excessive-pricing-in-mobile-broadband-in-botswana">On the backdrop of an outcry for reduction of internet data prices in the neighbouring South Africa, an expert engaged by the Botswana Communications Regulatory Authority (BOCRA) has revealed that there is no evidence of excessive pricing in mobile broadband in Botswana.</h4>

<p>According to an official from Inter Connect Communications (ICC), Eric Tyson, mobile broadband prices are favorable despite some prices being above regional average.</p>

<p>According to Tyson, the prices are even made more attractive to the consumers by the different packages offered to customers by the different networks. “The packages are just unbelievably attractive, customers are given a chance to choose between daily, weekly or monthly subscription depending on their budget,” said Noble Katse, BOCRA Director of Business Development. Meanwhile, the ICC officials also revealed that they have recommended that BOCRA should advise mobile network operators to remove the difference between on-net and off-net call costs.</p>

