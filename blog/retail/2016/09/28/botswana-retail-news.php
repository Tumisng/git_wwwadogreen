If Business Botswana president, Leta Mosienyane’s view that they are free to lobby anyone is anything to go by, then Vice President Mokgweetsi Masisi appears to be the next office bearer that the retailers and property developers with the help of Business Botswana will visit to get him to soften Seretse’s clutched fist. Their argument appears to hinge mostly on employment creation, a task that Masisi is overseeing; they will also point to their role in helping diversify the economy; and the potential consequences of forcing this law through.</p>

