<h2 id="eu-to-spend-244m-on-energy">EU to spend €244m on energy</h2>

<p>CHANDA MWENYA, Lusaka
GOVERNMENT has thanked the European Union (EU) for supporting economic growth and poverty alleviation in Zambia.
Minister of Finance Alexander Chikwanda said this during the launch of a photo exhibition on EU-Zambia development partnership that opened at National Museum in Lusaka yesterday.
Mr Chikwanda said Zambia has enjoyed close relations with the EU through the European Development Fund since 1975.
He said the EU has allocated €244 million for improved access to clean, reliable and affordable energy.
Under this component, the EU is upgrading the electricity distribution network and rehabilitation of the Kariba Dam.
Mr Chikwanda said the EU has allocated €110 million towards reducing rural poverty and improvement of livelihoods through conservation agriculture.
