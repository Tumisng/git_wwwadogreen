<h2 id="ziec-looks-at-diversifying-zambias-energy-mix">ZIEC looks at diversifying Zambia’s energy mix</h2>

<p>KELVIN KACHINGWE, Lusaka
BARELY a month after CUTS Lusaka hosted a public discussion on the energy crisis, another meeting got under-way in Lusaka last week to look at the same sector.
Just like the CUTS Lusaka-organised forum, the Zambia International Energy Conference (ZIEC) held at Mulungushi International Conference Centre brought together industry experts and like-minded professionals from finance, power, utility, renewable sectors, policy makers, economists and academics to discuss the future of the energy and environmental sectors in Zambia and what the sectors will look like in the next five years.
Held under the theme ‘Diversifying Zambia’s Energy Mix for a Resilient Power Sector’, the objective of the 2016 ZIEC was to accelerate the development and increased utilisation of renewable energy technologies in view of the current power deficit.
Stanbic Bank had partnered with the Ministry of Energy and Water Development in organising the meeting.
In his remarks, the bank’s chief executive officer, Charles Mudiwa, noted that as an institution operating in Zambia, Stanbic recognises that the country and indeed sub-Saharan Africa are endowed with sufficient energy resources to meet the needs of the region, though this sector still remains under-developed.</p>

