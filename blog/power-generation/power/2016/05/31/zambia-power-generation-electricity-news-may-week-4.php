<h2 id="ziec-looks-at-diversifying-zambias-energy-mix">ZIEC looks at diversifying Zambia’s energy mix</h2>

<p>Exploiting renewable energy sources and liberalising the energy sector to attract private-sector participation would certainly improve the cost effectiveness of producing electricity on the continent says Mr Darwish.
Therefore, the case for building renewable energy infrastructure in Sub-Saharan Africa is stronger than ever and positive experiences in lead markets such as South Africa and Kenya highlight successful strategies and best practices, he says.</p>

<p>“In addition, it’s important to note that falling technology costs, ambitious targets, resource abundance, a steady pipeline of high-quality projects, and the support of international donor partners are driving the rollout of renewable power in Africa,” Mr Darwish adds.</p>

<p>Solar and wind energy are especially promising, due to falling costs and resource abundance, he adds, and Africa can also leverage its significant power resources including geothermal power and hydropower.</p>

<p>“From solar-powered hospitals in Lagos to wind farms in Lake Turkana, viable renewable energy solutions are becoming a vital part of the continent’s energy mix. Through a host of off-grid technologies, increased renewable energy capabilities are beginning to provide direct and affordable power to rural and urban regions which have traditionally been beyond the reach of a traditional electricity grid system. In addition, innovative “pay-as-you-go” contracts, affordable technologies such as pico-solar units, and remote utility management software, are all examples of smart alternatives to the conventional grid approach,” says Mr Darwish – ITWeb</p>

