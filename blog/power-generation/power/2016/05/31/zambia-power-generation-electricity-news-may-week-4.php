                <h2 id="off-the-grid---africas-renewable-energy-challenge">Off the grid - Africa’s renewable energy challenge</h2>

<p>JOHANNESBURG – Key issues affecting electricity and energy supply include dependence on diesel power, erratic electricity grids and insufficient generation capacity. If the current power consumption trends continue, it is estimated that by the year 2030, 655 million people in Africa will be without access to power. Overall, it is estimated that approximately 600-million people in Sub-Saharan Africa currently lack access to electricity.In terms of trade and commerce, sourcing reliable power supply and renewable energy is a key priority for businesses in Africa, and the impact of shortfall and erratic supply continues to be felt across a number of sectors.
The CEO of beleaguered Zambia Telecommunications Company (Zamtel) blamed load-shedding, in addition to a weakening economy, for the company’s failure to make profit.</p>

<p>According to the Auditor General’s report, the company has not made any profit since 2012 when it was grabbed from Libya’s Lap Green Networks, who had bought it in 2010 and operated it profitably.
The report also claimed the company recorded a loss of K224 million between 2013 and in 2014.</p>

<p>At the heart of Africa’s power sector problem is the fact that insufficient generation capacity and limited electricity supply results in low access. Together with geographic constraints, these are barriers to the development of the electricity market in Africa says Issam Darwish, Executive Vice Chairman and CEO of IHS Towers Group.</p>

<p>The mobile telecommunications infrastructure provider recently commissioned a report Power Up, Delivering Renewable Energy in Africa, which has found that the continent requires up to US$90 billion to meet its current energy shortfall, that the market for pico-solar units has grown from almost zero in 2009, to 4.5 million in 2014, and, in January 2016, Africa saw its first solar bonds, a securitisation financial product for small scale off-grid solar projects.</p>

