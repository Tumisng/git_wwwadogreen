<h2 id="60-million-boost-to-zambia-power-grid">$60 million boost to Zambia power grid</h2>

<p>Mining is poised to receive a spinoff benefit from Monday’s announcement that the power grid in Zambia is to be boosted by a $60-million investment. In this latest transaction, Standard Chartered bank has partnered with the United States Agency for International Development (USAID) to provide a long-term loan to the State-owned Zambia Electricity Supply Corporation (Zesco). The loan has been described as one of the largest facilities that USAID has delivered within President Barack Obama’s ‘Power Africa’ partnership since the campaign was launched in 2013.  The loan will finance capital expenditure tor Zesco’s Lusaka transmission and distribution rehabilitation project as well as provide bridge financing to facilitate new connections to the grid. Standard Chartered Zambia CEO Andrew Okai described the latest loan as having both the “longest tenure and largest facility provided to a Zambian parastatal entity to date” and USAID/Zambia mission director Michael Yates said the latest funding would meet a critical need for a quarter of a million Zambians and the economy as a whole. Zesco MD Victor Mundende noted that support from USAID and Standard Chartered had already resulted in more than 15 000 new power connections being established across the organisation’s 39 000 km of power transmission and distribution lines. Standard Chartered corporate and institutional banking division CEO Simon Cooper made the point that Africa accounted for around a sixth of the world’s population, but generated only 4% of its electricity.</p>

