<h2 id="bee-advisory-council-mulls-various-new-radical-socioeconomic-transformation-proposals">BEE Advisory Council mulls various new radical socioeconomic transformation proposals</h2>

<h4 id="the-black-economic-empowerment-advisory-council-beeac-on-tuesday-convened-a-special-meeting-in-pretoria-to-unpack-new-proposals-that-emerged-from-a-prior-meeting-to-accelerate-the-councils-mandate-and-pursue-radical-socioeconomic-transformation">The Black Economic Empowerment Advisory Council (BEEAC) on Tuesday convened a special meeting in Pretoria to unpack new proposals, that emerged from a prior meeting, to accelerate the council’s mandate and pursue radical socioeconomic transformation.</h4>

<p>The workshop, convened by President Jacob Zuma, was aimed at scrutinising proposals to deal with the “systematic and structural implementation challenges” of the Broad-Based Black Economic Empowerment Act and engage on the proposed recommendations to realise government’s ambitions on radical socioeconomic transformation.</p>

<p>Trade and Industry Minister Dr Rob Davies, briefing the media prior to the workshop, said the proposals intended to strengthen empowerment in South Africa, improve the participation of the marginalised and apply the lessons learnt form preceding initiatives.
While details of the various proposals have not yet been released, with the council still mulling its options and ways forward post the workshop, Davies indicated that the council would work towards implementing various initiatives after securing Cabinet approval.</p>

<p>Davies noted that it was not a deliberate, decision-making meeting, but rather to engage ideas and gather information resulting in a clearer path forward.
Four focus areas would be used to forge a way forward to enable further opportunities to develop higher levels of inclusivity.</p>

<p>Following the meeting, Davies said procurement as a driver of black business and transformation emerged as a strong theme, with general agreement that procurement needed to be more carefully considered and crafted, with some suggestions that another approach be adopted that expanded the participation of black business.</p>

