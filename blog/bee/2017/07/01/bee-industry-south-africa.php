                <h2 id="sa-car-industry-in-r35bn-bee-plan">SA CAR INDUSTRY IN R3.5BN BEE PLAN</h2>

<h4 id="south-africas-automotive-industry-is-to-lobby-the-government-to-accept-its-planned-r35billion-transformation-fund-as-an-equity-equivalent-initiative-for-the-ownership-pillar-of-the-new-broad-based-black-economic-empowerment-bbbee-codes">South Africa’s automotive industry is to lobby the government to accept its planned R3.5billion transformation fund as an equity equivalent initiative for the ownership pillar of the new broad-based black economic empowerment (BBBEE) codes.</h4>

<p>Nico Vermeulen, the director of the National Association of Automobile Manufacturers of South Africa (Naamsa), the representative body of new vehicle manufacturers, said on Friday that the idea of establishing a sector code had been abandoned and the seven locally based original equipment manufacturers (OEMs) would operate independently in terms of the generic BBBEE scorecard.</p>

<p>However, Vermeulen said these OEMs had a major challenge in regard to ownership, because they did not have any equity for sale as they were wholly-owned subsidiaries of global multinational companies.</p>

<p>“In that regard, the OEM venture capital (transformation) fund was formulated and is now the subject of discussion and negotiation with the Department of Trade and Industry (dti),” he said.</p>

<p>Jeff Nemeth, the former chief executive of the Ford Motor Company of South Africa and president of Ford sub-Saharan Africa, told Business Report in 2015 that multinationals did not like to dilute their brands and found the ownership pillar in the new codes “very onerous”.</p>

<p>Nemeth said most sector specific codes had been more onerous than the general codes and this was one of the issues the industry would have to work its way through.</p>

<p>The automotive industry executives announced last week that the seven OEMs in the country planned to create a transformation fund that would allocate funds to develop black ownership in the car industry supply chain and vehicle dealership network.</p>

<p>They also provided details of the industry-developed vision and master plan to 2035.</p>

