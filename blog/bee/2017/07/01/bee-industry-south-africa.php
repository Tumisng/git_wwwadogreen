<h2 id="mining-charter-iii-may-be-problematic">MINING CHARTER III MAY BE PROBLEMATIC</h2>

<h4 id="johannesburg---while-the-transformation-imperatives-in-the-governments-mining-charter-iii-which-was-gazetted-last-month-were-laudable-its-unintended-consequences-are-problematic">Johannesburg - While the transformation imperatives in the government’s Mining Charter III, which was gazetted last month, were laudable, its unintended consequences are problematic.</h4>

<p>Stelio Zakkas, the manager at Monitor Deloitte, on Wednesday unpacked the six compliance areas of the charter during a discussion on the strategic implications of it. It was hosted by legal experts from Deloitte and Webber Wentzel and held in Sandton, Johannesburg, yesterday.</p>

<p>He told the 186 member audience comprising mining executives that ownership, one of the six elements which had attracted the most controversy, was the need for right holders to have a 30percent plus one black person shareholding within a year.</p>

<p>The 30percent target is split between an 8percent for employees through share ownership schemes, 14percent for black entrepreneurs and 8percent for mining communities.</p>

<p>Read also:  Mining sector faces unrest and other woes</p>

<p>The shares issued to a community must be held in a “community trust”, which must be created and managed by the yet to be constituted Mining Transformation and Development Agency (MTDA).</p>

<p>Zakkas argued that the MTDA should be a non-profit company, which was established as a National Government Business Enterprise under the Public Finance Management Act, 1999.</p>

<p>“Depending on the rights accruing to the shares held by the community trusts, the assigning of control of a plethora of community trusts to the MTDA may result in a violation of the Competition Act, 1998,” Zakkas said.</p>

<p>“In our view, the MTDA patronisingly deprives communities of the ability to manage their asset for their own benefit and, in fact, contradicts Mining Charter III’s objective of “redressing historical, socio-economic inequalities and ensuring broad based and meaningful participation of black persons in the mining and minerals industry”, he said.</p>

<p>In terms of black empowerment entrepreneurs, “BEE entrepreneur” was defined as a black-owned company or a black person who acquires an equity interest in a holder through a BEE transaction.</p>

<p>“While this is a seemingly good development, in our experience, the Department of Mineral Resources (DMR) does not view all ‘established’ black-owned companies as BEE entrepreneurs.</p>

<p>“Further, while this provision may have provided an efficient segue to the Department of Trade and Industry’s (dti) Black Industrialist Scheme, it is unclear if the DMR and dti have considered the possibility of jointly broaching this issue, leading to yet another instance of non-cohesive policy development,” he said.</p>

