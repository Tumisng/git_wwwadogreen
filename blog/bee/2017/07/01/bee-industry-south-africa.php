<h2 id="7-things-you-need-to-know-about-radical-economic-transformation">7 Things You Need To Know About Radical Economic Transformation</h2>

<h4 id="heres-what-the-ancs-policy-discussion-document-says-about-how-it-will-make-economic-transformation-a-reality">Here’s what the ANC’s policy discussion document says about how it will make economic transformation a reality.</h4>

<p>Radical economic transformation is “the gospel truth”, according to President Jacob Zuma. Both of the two main candidates touted to take over as ANC president – Nkosazana Dlamini-Zuma and Cyril Ramaphosa – have also embraced the phrase.</p>

<p>What it means for policy and politics, however, is still up for grabs. Does it mean unprecedented overhaul of the economy or is it old policy cloaked in a new catchy slogan? Is it an empty “ideological smokescreen” for state capture, as suggested by authors of the Betrayal of the Promise report, or a visionary call-to-action?</p>

<p>Here are (simplified) key strategies from the ANC’s policy discussion document on how they plan to realise economic transformation:</p>

<ol>
  <li>Re-industrialising South Africa’s economy</li>
</ol>

<p>The country must sever its old growth path, largely dependent on exporting raw materials. Instead, government must create the conditions for the economy to industrialise through manufacturing and beneficiation, or processing, of raw materials. It must use industrial policy - such as providing incentives for manufacturers - to promote a more diverse economy and generate mass employment.</p>

<ol>
  <li>Emphasis on black ownership (of land and the economy)</li>
</ol>

<p>Priority must be given to generating black ownership in emerging new, and old, sectors of the economy including mining and manufacturing. Revised BEE codes also need to be enforced across a range of sectors.</p>

<p>Targeted financial support - such as tax breaks and improved market access - for black enterpreneurs and small businesses is listed as crucial for equitable growth.</p>

<p>Supporting land reform and “returning the land to our people”, along with investment in farming equipment and technical skills transfer to beneficiaries, is a priority policy intervention.</p>

