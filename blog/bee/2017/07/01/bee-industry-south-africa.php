<h2 id="cyril-ramaphosa-radical-economic-transformation-let-me-explain-">CYRIL RAMAPHOSA: Radical economic transformation? Let me explain …</h2>

<h4 id="radical-economic-transformation-is-in-essence-about-building-a-more-equal-society-through-sustained-inclusive-growth">Radical economic transformation is, in essence, about building a more equal society through sustained inclusive growth</h4>

<p>The ANC’s programme of radical economic transformation has its roots in the Freedom Charter, which was adopted at the Congress of the People in Kliptown 62 years ago this week.</p>

<p>The Freedom Charter captures perfectly the intent and, to some extent, the content of radical economic transformation. It was at the Congress of the People that representatives of the people of this country gathered to declare that: “The People Shall Share in the Country’s Wealth.”</p>

<p>Given the extent of dispossession, discrimination, exploitation and exclusion, this call in the Freedom Charter was a call for radical and fundamental economic transformation.</p>

<p>Over the intervening six decades, the principles of redress, redistribution, social justice and equality have been at the centre of ANC economic policy. These principles have underpinned the ANC’s policies in government, notably in the Reconstruction and Development Programme (RDP) and the National Development Plan (NDP).</p>

<p>At the ANC’s Mangaung Conference in 2012, the organisation adopted the NDP as an overarching framework for the second phase of our democratic transition – the pursuit of socio-economic freedom. The Mangaung Conference recognised that in the nearly two decades since the advent of democracy, political freedom had largely been achieved. The priority now was to pursue economic freedom.</p>

