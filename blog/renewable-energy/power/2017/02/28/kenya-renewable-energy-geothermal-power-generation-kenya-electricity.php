                <h1 id="mhps-receives-order-for-two-sets-of-geothermal-power-generation-facilities-for-kenya-electricity-generating-company">MHPS Receives Order for Two Sets of Geothermal Power Generation Facilities for Kenya Electricity Generating Company</h1>

<h5 id="yokohama-japan-feb-28-2017---jcn-newswire---mitsubishi-hitachi-power-systems-ltd-mhps-together-with-mitsubishi-corporation-and-h-young--company-east-africa-ltd-have-received-a-full-turnkey-order-to-provide-power-generation-facilities-to-kenya-electricity-generating-company-limited-kengen-including-two-sets-of-70-megawatt-mw-class-steam-turbines-generators-and-auxiliaries-the-equipment-on-order-will-be-installed-at-the-olkaria-v-geothermal-power-plant-in-nakuru-district-in-the-central-area-of-the-republic-of-kenya-the-new-plant-is-scheduled-to-go-on-stream-in-2019">Yokohama, Japan, Feb 28, 2017 - (JCN Newswire) - Mitsubishi Hitachi Power Systems, Ltd. (MHPS), together with Mitsubishi Corporation and H. Young &amp; Company (East Africa) Ltd., have received a full-turnkey order to provide Power Generation Facilities to Kenya Electricity Generating Company Limited (KenGen), including two sets of 70 megawatt (MW) class steam turbines, generators and auxiliaries. The equipment on order will be installed at the Olkaria V Geothermal Power Plant in Nakuru district in the central area of the Republic of Kenya. The new plant is scheduled to go on-stream in 2019.</h5>

<p>The Olkaria V Geothermal Power Plant site is located approximately 100 kilometers northwest of Nairobi, Kenya’s capital city, at an elevation of some 2,000 meters in the Great Rift Valley. This new project will expand existing power plant facilities in the Olkaria geothermal field with the aim of alleviating severely stretched power supplies in Kenya’s urban areas while increasing power supply from renewable energy to reduce carbon emissions The project is being carried out with an ODA (Official Development Assistance) loan arrangement, extended to KenGen by the Japanese Government through the Japan International Cooperation Agency (JICA).</p>

<p>MHPS will be responsible for the design of the geothermal facilities and will supply the steam turbines, generators, condensers, and other main auxiliaries. Applying its extensive expertise as an EPC (engineering, procurement and construction) contractor, MHPS will also dispatch Technical Advisors to the site to assist H. Young with expertise in installation and commissioning.</p>

<p>Kenya ranks ninth worldwide in terms of geothermal power generation capacity. In recent years demand for electric power has been increasing in step with the country’s steady economic growth. In response, KenGen is focusing on building new geothermal plants and expanding the output of existing facilities. MHPS has already supplied six sets of power generating equipment for the Olkaria I and II geothermal power plants with 150 MW total output. This latest order was awarded in recognition of such equipment’s outstanding operating record as well as MHPS’s technological strength and EPC execution capability.</p>

<p>MHPS is in a prime position to provide comprehensive solutions in both thermal and geothermal power generation systems. In the geothermal power sector to date, MHPS has received more than 100 orders from 13 countries. The total capacity of these units exceeds 3,000 MW. MHPS is a world leader in the technical development and provision of geothermal power generation facilities. At the Sixth Tokyo International Conference on African Development (TICAD VI) held in Nairobi in August 2016, former President and CEO Takato Nishizawa (recently elected as Vice Chairman) highlighted MHPS’s commitment to aid Africa’s development through provision of advanced environmental technologies and outstandingly efficient power generating equipment.</p>

<p>MHPS is committed to working closely with related institutions in implementing various programs to enact the Japanese Government’s policy for exporting high-quality infrastructure to meet rising demand for geothermal power in Kenya and throughout Africa. MHPS will also proactively propose solutions in geothermal power generation equipment and facilities not only in Africa but globally, as it seeks to provide highly efficient and environmentally friendly technologies to curb global warming and also to provide reliable and cost effective electricity to its customers.</p>

