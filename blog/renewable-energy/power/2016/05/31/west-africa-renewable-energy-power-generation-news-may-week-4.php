<h2 id="rwandas-gas-to-electricity-and-railway-projects-will-make-many-smile">Rwanda’s gas-to-electricity and railway projects will make many smile</h2>

<p>Methane-fired power plant on Lake Kivu to generate clean energy.</p>

<p>IN THE past two weeks Rwanda has made two big announcements, which will have far-reaching implications for cement companies and consumers in the country, as well as for the building and construction sector in a region that has some of the highest cement prices in Africa.</p>

<p>Rwanda has embarked on two big infrastructure projects – first is (the recently launched) methane-fired power plant on Lake Kivu to generate clean energy. The KivuWatt gas-water extraction project is the only one operating in the world, according to ContourGlobal LP, the U.S. company that built the facility.</p>

<p>It is expected to produce 100 megawatts within three years as it extracts gas found in the lake’s deep waters to generate low-carbon emission power, also reducing the risk of a catastrophic release of methane.</p>

<p>In a country that has a current generation capacity of 186 megawatts, and peak demand of 105 megawatts, KivuWatt represents an effective doubling of power available in the country.</p>

