                <h2 id="afdb-and-the-west-africa-power-pool-grant-19-million-for-nigeria-benin-interconnector-reinforcement-project">AfDB and the West Africa Power Pool Grant $1.9 Million for Nigeria-Benin Interconnector Reinforcement Project</h2>

<p>The African Development Bank (AfDB) and the West Africa Power Pool (WAPP), a specialized institution of ECOWAS have signed a grant agreement worth $1.9 million to support the Nigeria-Benin Interconnector Reinforcement Project.</p>

<p>The grant will go towards the construction of a 330 kV double circuit high-voltage transmission line from Erukan, Nigeria to Sakete, Benin.</p>

<p>AfDB Acting Vice-President Stefan Nalletamby said: “Energy is one of the most essential requirements for Africa’s development and a key pillar of the Bank’s High 5s vision. The project will allow inclusive economic growth needed to transform the lives and livelihoods of many in the West African region.”</p>

<p>The project will ensure stable integration of the national electricity networks and supply reliable electricity at affordable cost in theECOWAS region.</p>

