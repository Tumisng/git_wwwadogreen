<h2 id="ghana-home-energy-africa-to-develop-solar-pv-project">Ghana: Home Energy Africa to develop solar PV project</h2>

<p>In West Africa, Home Energy Africa, a renewable energy solutions provider, has obtained a $705,000 grant from the US Trade and Development Agency (USTDA) for the development of a solar PV power generation project in Ghana.</p>

<p>Projected to begin construction in 2017, local media reported that this solar project will generate 100MW of power, providing electricity to approximately 80,000 average homes in the country.</p>

<p>The agreement between the two countries was signed by Robert P. Jackson, the US Ambassador to Ghana and Charles Sena Kwadzo Ayenu, CEO of Home Energy Africa.</p>

<p>Inadequate power supply</p>

<p>According to the media, Jackson said the unreliable and inadequate power supply is a major challenge to businesses and entrepreneurs and a constraint to the economy.</p>

<p>He said: “Presently Ghana has 2,450MW of installed capacity. The government of Ghana aspires to double that capacity to 5,000MW this year, including 10% from renewable sources.”</p>

<p>“Through programmes such as Power Africa, the Partnership for Growth and the Millennium Challenge Corporation, we are cooperating with government, the private sector and others to make Ghana’s future brighter, literally brighter.”</p>

<p>Jackson added: “It is exciting to see the great things that can happen when American and Ghanaian companies partner. We are able to bring products and services that benefit Ghanaians throughout this country.”</p>

<p>Technical assistance to Home Energy Africa</p>

<p>Jackson explained that the USTDA grant would go towards providing technical assistance to Home Energy Africa through GreenMax Capital Advisors, an American firm, in finalising the legal and financial details necessary to implement the project.</p>

<p>The assistance includes preparation for power purchase agreement negotiations with the Electricity Company of Ghana, services contracts and financial arrangements.</p>

<p>Commenting on the deal, Ayenu said the signing of the grant was the last barrier that the company has had to cross for work to begin on the project. He added that the firm has also acquired a 30% equity funding agreement for the $150 million project.</p>

