<h2 id="why-cbn-disbursed-n5546-billion-to-24-power-firms">WHY CBN DISBURSED N55.46 BILLION TO 24 POWER FIRMS</h2>

<p>The Governor of the Central Bank of Nigeria (CBN), Godwin Emefiele, recently disclosed that the bank disbursed N55.46 billion to 24 power firms as an electricity market stabilisation facility. This was made known during the fourth trance disbursement of the facility in Lagos.</p>

<p>According to Godwin Emefiele, this disbursement, in collaboration with the federal government, is a major milestone for the bank which will help address the challenges of the sector. It will also feature the signing of a Power Purchase Agreement (PPA) by the Nigerian Bulk Electricity Traders (NBET), which will, in turn, signal activation of industry contracts for power generation under a contract based market.</p>

<p>Here are six things happening in the power sector according to Godwin Emefiele:</p>

<p>There are two new entrants into the scheme between the federal government in collaboration with the CBN. These two new entrants were the two distribution companies in Benin and Jos.</p>

<p>The others are eight generating companies that include two independent power projects by Agip/Okpai and Shell and six NIPP plants in Alaoji, Geregu, Ihovbor, Olorunsogo2, Omotosho2 and Sapele2.</p>

<p>The N55.46 billion was disbursed to 24 power firms in the following order — 14 generating companies and the Nigerian Independent Power Project (NIPP) got N35.83 billion, six gas companies received N10.49 billion, three distribution companies received N8.67 billion and a service provider received N459.68 million.</p>

