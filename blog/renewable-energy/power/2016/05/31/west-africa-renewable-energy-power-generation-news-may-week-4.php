<h2 id="home-energy-africa-to-develop-solar-in-ghana">Home Energy Africa To Develop Solar In Ghana</h2>

<p>The agreement between the two countries was signed by Robert P. Jackson, the US Ambassador to Ghana, and Charles Sena Kwadzo Ayenu, CEO of Home Energy Africa.</p>

<p>Home Energy Africa, which specializes in the development and sales of renewable energy products for businesses, governments, and residential homes in Africa, has obtained a $705,000 grant from the US Trade and Development Agency (USTDA) for the development of a solar PV power generation project in Ghana.</p>

<p>Projected to begin construction in 2017, ESI Africa reports that this solar project will generate 100 MW of power, providing electricity to approximately 80,000 average homes in the country.</p>

<p>The agreement between the two countries was signed by Robert P. Jackson, the US Ambassador to Ghana, and Charles Sena Kwadzo Ayenu, CEO of Home Energy Africa.</p>

<p>“Lack of power is a challenge we see across sub-Saharan Africa. Two out of three people in this region lack access to electricity. That hinders business, and it hinders prosperity. We’ve made increasing access to power one of the top priorities for our bilateral relationship. Today’s grant is just one more way we’re bringing together government and the private sector to make Ghana’s future brighter,” said Jackson.</p>

